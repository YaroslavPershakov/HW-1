public class Types {
    public static void main(String[] args){
        //Целочисленные
        byte b = 10;
        short s = 30000;
        int i = 341;
        long l = 4104923948L;
        //С плавающей точкой
        float f = 34.10332f;
        double d = 0.0000041;
        //Логический
        boolean bl = true;
        //Символьный
        char ch = 'C';
        System.out.println(b + ", " + s + ", " + i + ", " + l + ", " + f + ", " + d + ", "+ bl + ", " + ch);
    }
}
